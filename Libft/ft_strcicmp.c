#include "libft.h"

int		ft_strcicmp(const char *s1, const char *s2)
{
	int		d;

	while (*s1 != '\0' || *s2 != '\0')
	{
		d = ft_tolower(*s1) - ft_tolower(*s2);
		if (d != 0)
			return (d);
		s2++;
		s1++;
	}
	return (0);
}
